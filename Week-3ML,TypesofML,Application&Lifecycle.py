# Machine Learning Lifecyle
# 1. Defining the Problem: Clearly identify the real-world problem to be solved.
# 2. Data Collection: Gather necessary data from various sources.
# 3. Data Cleaning and Preprocessing: Resolve data quality issues and prepare the data for analysis.
# 4. Exploratory Data Analysis (EDA): Analyze data to identify patterns, outliers, and trends.
# 5. Feature Engineering and Selection: Enhance data features and select relevant ones to improve model performance.
# 6. Model Selection: Choose suitable models based on the problem type and data characteristics.
# 7. Model Training: Train the model using a split of training and validation datasets.
# 8. Model Evaluation and Tuning: Assess and optimize the model using relevant metrics.
# 9. Model Deployment: Implement the model in a production environment for real-time predictions.
# 10. Model Monitoring and Maintenance: Regularly check and update the model to maintain accuracy.

# Types of Machine Learning
# 1. Supervised Machine Learning

# Supervised learning algorithms are trained on the labeled dataset. They learn to map input features to targets based on labeled training 
# data. There are two main types of supervised learning:

# Regression: Regression algorithm learns to predict continuous values based on input features.
# Classification: Classification algorithm learns to assign input data to a specific category or class based on input features. 
# The output labels in classification are discrete values.

# 2. Unsupervised Machine Learning

# Unsupervised learning algorithm learns to recognize patterns in data without being explicitly trained using labeled examples. 
# The goal is to discover the underlying structure or distribution in the data.

# There are two main types of unsupervised learning:

# Clustering: Clustering algorithms group similar data points together based on their characteristics. The goal is to identify groups, 
# or clusters, of data points that are similar to each other, while being distinct from other groups.
# Dimensionality reduction: Dimensionality reduction algorithms reduce the number of input variables in a dataset while preserving as 
# much of the original information as possible. This is useful for reducing the complexity of a dataset and making it easier to visualize
# and analyze.

# 3. Reinforcement Machine Learning

# In Reinforcement Learning, an agent learns to interact with an environment by performing actions and receiving rewards or penalties 
# based on its actions. The goal of reinforcement learning is to learn a policy, which is a mapping from states to actions, that maximizes the expected cumulative reward over time.

# There are two main types of reinforcement learning:

# Model-based reinforcement learning: The agent learns a model of the environment, including the transition probabilities between states 
# and the rewards associated with each state-action pair. The agent then uses this model to plan its actions in order to maximize its 
# expected reward.

# Model-free reinforcement learning: The agent learns a policy directly from experience without explicitly building a model of the 
# environment. The agent interacts with the environment and updates its policy based on the rewards it receives.
# Various Applications of Machine Learning

# Automation: Machine learning, which works entirely autonomously in any field without the need for any human intervention. For example, 
# robots perform the essential process steps in manufacturing plants.
# Finance Industry: Machine learning is growing in popularity in the finance industry. Banks are mainly using ML to find patterns inside 
# the data but also to prevent fraud.

# Government organization: The government makes use of ML to manage public safety and utilities. Take the example of China with its 
# massive face recognition. The government uses Artificial intelligence to prevent jaywalking.

# Healthcare industry: Healthcare was one of the first industries to use machine learning with image detection.

# Marketing: Broad use of AI is done in marketing thanks to abundant access to data. Before the age of mass data, researchers develop 
# advanced mathematical tools like Bayesian analysis to estimate the value of a customer. With the boom of data, the marketing 
# department relies on AI to optimize customer relationships and marketing campaigns.

# Retail industry: Machine learning is used in the retail industry to analyze customer behavior, predict demand, and manage inventory. 
# It also helps retailers to personalize the shopping experience for each customer by recommending products based on their past 
# purchases and preferences.

# Transportation: Machine learning is used in the transportation industry to optimize routes, reduce fuel consumption, and improve the 
# overall efficiency of transportation systems. It also plays a role in autonomous vehicles, where ML algorithms are used to make 
# decisions about navigation and safety.

# Limitations of Machine Learning
# Data Availability: Machines require sufficient data to learn; without it, learning cannot occur.

# Diversity in Data: A lack of diversity within the dataset can significantly hinder machine learning processes.

# Need for Heterogeneity: Diverse and varied data are crucial for extracting meaningful insights.

# Impact of Low Variation: Algorithms struggle to derive information from datasets with minimal variation.

# Observations Per Group: It is recommended to have at least 20 observations per group to ensure effective learning.
